             'user_id' => Auth::user()->id,
             'follow_id' => $id
         ];

         Follow::create($data);

         return redirect(route('user.profile' , $id));
    }
    public function unfollow($id) {
        DB::table('follows')->where('user_id' ,  Auth::user()->id )
        ->where('follow_id' , $id)->delete();
        return redirect(route('user.profile' , $id));
    }

